package mx.gob.segob.dgtic.comun.util;

import java.util.UUID;

/**
 *  Clase de apoyo para manejo la generacion de un identificador universal (UUID)
 */
public class UUIDUtil {

	/**
	 * Constructor de utiler&iacute;a para acceso completamente est&aacute;tico
	 */
	private UUIDUtil() {
		throw new IllegalStateException("Utility class");
	}

	/**
	 * Obtienen el identificador unico universal (UUID).
	 *
	 * @return El identificador unico universal (UUID)
	 */
	public static String getUUID(){
		UUID uuid = UUID.randomUUID();
		return uuid.toString();
	}
}
